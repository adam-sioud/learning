package of0.lf.oving0;

public class Loops {

    public static void main(String[] args) {
        for (int i = 3; i < 10; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }

        int j = 0;
        while (j < 10) {
            System.out.println(j);
            j++;
        }
    }
}
